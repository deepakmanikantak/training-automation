package com.trainee.aizaz.ApiAutomation.pojo;

public class Wind {
    private Float speed;
    private Integer deg;

    public void setSpeed(Float speed) {
        this.speed = speed;
    }

    public void setDeg(Integer deg) {
        this.deg = deg;
    }

    public Float getSpeed() {
        return speed;
    }

    public Integer getDeg() {
        return deg;
    }
}
